    private void cargarTabla() {
        
        //definir el modelo que queremos que tenga la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
          
            //que fila y columna no sean editables
            @Override
            public boolean isCellEditable (int row, int column){
              return false;
          }  
        };
        
        //establecemos los nombres de las columnas
        String titulos[] = {"Num", "Nombre", "Animal", "Raza", "Color", "Alergico", "At. Esp", "Dueño", "Cel"};
        modeloTabla.setColumnIdentifiers(titulos);
        
   
    //carga de los datos desde la base de datos
    List<Mascota> listaMascotas = control.traerMascotas();
    
    //recorrer la lista y mostrar cada uno de los elementos en la tabla
    if(listaMascotas != null){
        for(Mascota masco : listaMascotas){
            Object[] objeto = {masco.getNumCliente(), masco.getNombre(), masco.getAnimal(), masco.getRaza(), masco.getColor(), masco.getAlergico(), masco.getAtencionEspecial(), masco.getDuenio().getNombre(), masco.getDuenio().getCelDuenio() };
        
        modeloTabla.addRow(objeto);
        }
    }
    tblDatos.setModel(modeloTabla);
    }
