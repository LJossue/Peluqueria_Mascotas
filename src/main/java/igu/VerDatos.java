    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
//controlo que la tabla no este vacia

if(tblDatos.getRowCount() > 0){

    //controlo que se haya seleccionado un registro

    if(tblDatos.getSelectedRow() != -1){
        //obtengo el id de la mascota a borrar
        int numCliente = Integer.parseInt(String.valueOf(tblDatos.getValueAt(tblDatos.getSelectedRow(), 0)));
        //llamo al metodo borrar
        control.eliminarMascota(numCliente);
        
        //aviso al usuario
        mostrarMensaje("Mascota eliminada correctamente", "Info", "Eliminar Mascota");
        cargarTabla();
        
    }else{
        mostrarMensaje("No seleccionó ninguna mascota", "Error", "Error al eliminar");
    }
}else{
    mostrarMensaje("No hay ninguna mascota", "Error", "Error al eliminar");
}
    }//GEN-LAST:event_btnEliminarActionPerformed

  public void mostrarMensaje(String mensaje, String tipoMensaje, String titulo){
       JOptionPane optionPane = new JOptionPane(mensaje);
       if(tipoMensaje.equals("Info")){
                   optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);

       }else if(tipoMensaje.equals("Error")){
                   optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);

       }
        JDialog dialog = optionPane.createDialog(titulo);
                dialog.setAlwaysOnTop(true);
                dialog.setVisible(true);
  }

