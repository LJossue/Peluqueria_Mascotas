    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
if(tblDatos.getRowCount() > 0){

    //controlo que se haya seleccionado un registro

    if(tblDatos.getSelectedRow() != -1){
        //obtengo el id de la mascota a editar
        int numCliente = Integer.parseInt(String.valueOf(tblDatos.getValueAt(tblDatos.getSelectedRow(), 0)));
        //abro el panel de editar
        ModificarDatos pantModif = new ModificarDatos(numCliente);
        pantModif.setVisible(true);
        pantModif.setLocationRelativeTo(null);
        this.dispose();
       
    }else{
        mostrarMensaje("No seleccionó ninguna mascota", "Error", "Error al editar");
    }
}else{
    mostrarMensaje("No hay ninguna mascota", "Error", "Error al editar");
}

    }//GEN-LAST:event_btnEditarActionPerformed

